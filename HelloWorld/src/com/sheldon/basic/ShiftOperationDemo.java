package com.sheldon.basic;

public class ShiftOperationDemo {

	public static void main(String[] args) {
		int i = -1;
		System.out.println(Integer.toBinaryString(i));
		System.out.println(Integer.toBinaryString(i>>>10));
		System.out.println(Integer.toBinaryString(i>>10));
		System.out.println(Integer.toBinaryString(i<<10));
	}
	
}
